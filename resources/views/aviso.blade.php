			<h3 class="headline centered margin-top-75">
				<span class="margin-top-35">En cumplimiento con lo establecido por la Ley Federal de Protección de Datos Personales en Posesión de Particulares publicada en el Diario Oficial de la Federación el día 5 de Julio de 2010, se extiende el presente Aviso de Privacidad.</span>

					<strong class="headline-with-separator margin-top-60">a) Responsable del tratamiento de datos personales</strong>
					<span class="margin-top-15">Cuxeya.org., es una Institución de Asistencia Privada en la que día a día trabajamos para atender servicios y soluciones en problemas sociales, ofreciéndoles la oportunidad de vivir y disfrutar de una sociedad digna y plena al tiempo que les facilitamos las herramientas necesarias para que se construya un futuro brillante en beneficio de la sociedad, cuyas oficinas administrativas se encuentran ubicadas en Av. Horacio 632, Polanco, Polanco IV Secc, Miguel Hidalgo, 11550 Ciudad de México, CDMX.</span>

					<span class="margin-top-15">Por medio del presente documento se hace de su conocimiento que sus datos personales serán tratados estrictamente para los fines que a continuación se describen:</span>

					<strong class="headline-with-separator margin-top-60">b) Datos Personales</strong>
					<span class="margin-top-15">Los datos personales que serán tratados por Cuxeya.org, consistentes en información personal del titular, la cual que puede comprender: su nombre completo, domicilio, números de teléfono, lugar y fecha de nacimiento, dirección de correo electrónico, situación socioeconómica, datos patrimoniales y financieros, cuenta bancaria para otorgamiento de donativos, Registro Federal de Contribuyentes, CURP, entre otra estrictamente indispensable para los fines destinados y que se describirán con posterioridad. Dentro de la información anteriormente descrita, se encuentran datos personales sensibles.</span>

					<span class="margin-top-15">En caso de no contar con sus datos personales no estaríamos en posibilidad de llevar a cabo los fines para los cuales se requieren, por lo que Cuxeya.org no tendría ningún tipo de responsabilidad derivado de ello.</span>

					<strong class="headline-with-separator margin-top-60">c) Finalidades del Tratamiento de Datos Personales</strong>
					<span class="margin-top-15">Cuxeya.org, recabará sus datos personales sin fines de divulgación o utilización comercial, y será únicamente para los siguientes fines:</span>

					<span class="margin-top-15">Control y Estadística de donativos</span>
					<span class="margin-top-15">Base de datos de donantes para generar agradecimientos</span>
					<span class="margin-top-15">El cumplimiento de obligaciones legales y fiscales.
					<span class="margin-top-15">Los datos personales serán guardados bajo la más estricta confidencialidad y no se les podrá dar un uso distinto a los antes mencionados, salvo que medie un cambio en este Aviso de Privacidad.</span>

					<span class="margin-top-15">Una vez que se cumpla la finalidad del tratamiento de datos personales, éstos serán bloqueados con el único propósito de determinar posibles responsabilidades en relación con su tratamiento, hasta el plazo de prescripción legal.</span>

					<span class="margin-top-15">Durante dicho periodo, los datos personales no podrán ser objeto de tratamiento y transcurrido éste, se procederá a su cancelación en la base de datos que corresponde.</span>

					<strong class="headline-with-separator margin-top-60">d) Recolección de los Datos Personales Para la recolección de datos personales, seguimos todos los principios que marca la Ley Federal de Protección de Datos Personales en Posesión de Particulares como la licitud, calidad, consentimiento, responsabilidad.</strong>

					<span class="margin-top-15">Los datos personales serán recabados directamente del titular de forma personal o por cualquier otra tecnología legalmente permitida.</span>

					<span class="margin-top-15">La información que recibimos y almacenamos a través de nuestra página web, cuando y usted interactúa en nuestros sitios, es principalmente información técnica como su dirección IP, el sistema operativo con el cuál esta interactuando desde su equipo y su tipo de navegador, la dirección de un sitio web de referencia, en su caso, y la ruta que siga durante su recorrido por nuestra página web. Usamos “cookies” para reconocerlo cuando utilice o regrese a nuestros sitios. Una “cookie” es un pequeño archivo de texto que un sitio web o un correo electrónico puede salvar en su navegador y almacenarlo en su disco duro.</span>

					<span class="margin-top-15">Usted puede elegir navegar en nuestros sitios web sin aceptar las cookies. Las cookies nos permiten reconocerlo dentro de nuestras páginas con contenido web, y respaldan sus operaciones con nosotros.</span>

					<span class="margin-top-15">Sin las cookies habilitadas, usted podrá navegar en nuestros sitios web, pero es posible que no pueda utilizar algunas características del sitio web. Se recaba esta información para permitir que los sitios web operen correctamente, evaluar el uso del sitio web y apoyar las campañas promocionales.</span>

					<span class="margin-top-15">Además, podremos obtener información del titular de otras fuentes permitidas por la ley, tales como directorios telefónicos o laborales, bolsas de trabajo, referencias de otras empresas o particulares, bases de datos públicas de cualquier entidad o dependencia pública o privada, entre otras.</span>

					<strong class="headline-with-separator margin-top-60">e) Opciones y medios para limitar el uso o divulgación de los datos</strong>
					<span class="margin-top-15">En Cuxeya.org se cuenta con las medidas de seguridad, administrativas, técnicas y físicas necesarias y suficientes para proteger sus datos personales contra daño, pérdida, alteración, destrucción, uso, acceso o tratamiento no autorizado.</span>

					<span class="margin-top-15">Los datos personales son salvaguardados en bases de datos y equipos de cómputo que cuentan con la seguridad necesaria para prevenir fugas de información. Controles de acceso físico y lógico, controles ambientales, sistemas de protección anti intrusos (proporcionados por el proveedor), herramientas de protección antivirus y filtrado web son algunas de las herramientas utilizadas para mantener la seguridad de los datos en los sistemas de información de Cuxeya.org.</span>

					<span class="margin-top-15">Las herramientas de seguridad informática mencionadas en el párrafo anterior están apoyadas por una política interna de seguridad de la información que explica a los empleados las consideraciones de seguridad que deben tomar en cuenta al utilizar un sistema informático y es reforzada constantemente.</span>

					<span class="margin-top-15">Por ello, se hacen de su conocimiento las opciones que usted tiene para solicitar la limitación del uso o divulgación de sus datos personales, materia de este Aviso:</span>

					<span class="margin-top-15">1) Por escrito enviado al Encargado del Tratamiento de Datos Personales (en el domicilio señalado en el inciso f) siguiente); o vía correo electrónico dirigido a la dirección datos@cuxeya.org</span>

					<span class="margin-top-15">2) Vía telefónica con el Encargado del Tratamiento de Datos Personales al teléfono (55) 41612253.</span>

					<strong class="headline-with-separator margin-top-60">f) Encargado del Tratamiento de los Datos Personales</strong>

					<span class="margin-top-15">El Encargado de Tratamiento de Datos Personales en Cuxeya.org es Lic. Jorge Adrián Calderón, con horarios de atención de 09:00 a 18:00 horas de lunes a viernes, correo electrónico datos@cuxeya.org, teléfono directo (55) 41612253.</span>

					<strong class="headline-with-separator margin-top-60">g) Medios para ejercer los derechos de acceso, rectificación, cancelación u oposición, de conformidad con lo dispuesto en esta Ley Usted podrá ejercer sus derechos ARCO (acceso, rectificación, cancelación y/u oposición) contactando directamente al Encargado del Tratamiento de Datos Personales vía correo electrónico: datos@cuxeya.org, la solicitud ARCO deberá contener y acompañar lo que señala el artículo 29 de la Ley Federal de Protección de Datos Personales en Posesión de los Particulares, mismo que a la letra dice:</strong>

					<span class="margin-top-15">El nombre y domicilio del titular u otro medio para comunicarle la respuesta a su solicitud</span>
					<span class="margin-top-15">Los documentos que acrediten su identidad o, en su caso, la representación legal del titular</span>
					<span class="margin-top-15">La descripción clara y precisa de los datos personales respecto de los que se busca ejercer alguno de los derechos.</span>
					<span class="margin-top-15">Cualquier otro elemento o documento que facilite la localización de los datos personales, así como cualquier otro documento que exija la legislación vigente al momento de la presentación de la solicitud. Cuxeya.org no estará obligado a cancelar los datos personales cuando se trate de alguno de los supuestos establecidos en la Ley Federal de Protección de Datos Personales en Posesión de los Particulares, entre otras las siguientes: que se refiera a las partes de un contrato privado, social o administrativo y sean necesarios para su desarrollo y cumplimiento; deban ser tratados por disposición legal; se obstaculice actuaciones judiciales o administrativas vinculadas a obligaciones fiscales, la investigación y persecución de delitos o la actualización de sanciones administrativas; sean necesarios para proteger los intereses jurídicamente tutelados del titular; sean necesarios para realizar una acción en función del interés público; sean necesarios para cumplir una obligación legalmente adquirida por el titular y sean objeto de tratamiento para la prevención o para el diagnóstico médico o la gestión de servicios de salud, siempre que dicho tratamiento se realice por un profesional de la salud sujeto a un deber de secreto.</span>
					<span class="margin-top-15">Para tener mayor información respecto al ejercicio de sus derechos ARCO puede comunicarse directamente con el Encargado del Tratamiento de Datos Personales cuyos datos aparecen en el inciso f) del presente Aviso de Privacidad.</span>

					<strong class="headline-with-separator margin-top-60">h) Transferencias de datos que se efectúen</strong>
					<span class="margin-top-15">Los datos personales jamás serán divulgados, ni compartidos con terceros, en el entendido de que no se considera como tercero a las áreas internas y diversas Direcciones que formen parte de Cuxeya.org que requieran tener los datos personales para los fines antes mencionados. La única transferencia de datos a terceros será en todo caso para dar cumplimiento a obligaciones legales y fiscales que deriven de la naturaleza del donativo con el titular de los datos personales.</span>

					<span class="margin-top-15">Por lo tanto, el titular al dar su consentimiento a este Aviso de Privacidad, o bien al no oponerse de forma manifiesta dentro del plazo de 15 (quince) días a partir de que tenga conocimiento del mismo acepta que sus datos personales sean tratados para las finalidades antes señaladas.</span>

					<strong class="headline-with-separator margin-top-60">i) Revocación</strong>
					<span class="margin-top-15">El titular de los datos personales podrá revocar su consentimiento para el tratamiento de datos personales en cualquier momento, debiendo, únicamente para tales efectos, enviar una solicitud por escrito al Encargado de los Datos Personales, ya sea por mensajería al domicilio o al correo electrónico mencionado en el inciso f) del presente Aviso.</span>

					<span class="margin-top-15">Tendremos un plazo máximo de 20 días hábiles para informarle sobre la procedencia de la misma y si resulta procedente, se haga efectiva la misma dentro de los quince días hábiles siguientes a la fecha en que se comunica la respuesta al correo electrónico que nos proporcione para tales efectos.</span>

					<strong class="headline-with-separator margin-top-60">j) Consentimiento del Titular</strong>
					<span class="margin-top-15">Consiento que mis datos personales sensibles sean tratados conforme a los términos y condiciones del presente Aviso de Privacidad.</span>

					<strong class="headline-with-separator margin-top-60">k) Cambios al Aviso de Privacidad</strong>
					<span class="margin-top-15">Nos reservamos el derecho de cambiar este Aviso de Privacidad en cualquier momento.</span>

